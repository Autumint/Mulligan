-- Red Deck
SMODS.Achievement {
    key = "beast_red",
    pos = { x = 0, y = 0 },
    unlock_condition = function()
        return G.GAME.blind
            and G.GAME.blind.config.blind.key == "bl_tdec_beast"
            and G.GAME.selected_back
            and G.GAME.selected_back.effect.center.key == "b_red"
            and G.GAME.chips > G.GAME.blind.chips
    end
}

